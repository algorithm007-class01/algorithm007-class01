// 反转法
// 1）取模，避免 k 大于数组长度
// 2）反转整个数组
// 3）反转前 k 个元素
// 4）反转剩下的元素
func rotate(nums []int, k int) {
	k %= len(nums)
	reverse(nums)
	reverse(nums[:k])
	reverse(nums[k:])
}

func reverse(slice []int) {
	for left, right := 0, len(slice)-1; left < right; left, right = left+1, right-1 {
		slice[left], slice[right] = slice[right], slice[left]
	}
}

